/**
 * Small global calibration surface for racecraft behavior.
 *
 * These are policy/controller quantities shared by every track. Geometry,
 * vehicle dimensions, rule clearances, and state-machine release thresholds
 * are deliberately not calibration dimensions.
 */
export interface RacecraftCalibration {
  predictionHorizonSeconds: number;
  blueFlagTimeToCatchSeconds: number;
  qualifyingTimeToCatchSeconds: number;
  brakingPrudence: number;
  towRangeM: number;
  wakeSpreadRate: number;
  towDragReduction: number;
  dirtyAirMarginPenalty: number;
  mistakeUtilizationRate: number;
  nearRubClearanceMetres: number;
  defensiveBlockNoticeSeconds: number;
}

export interface RacecraftCalibrationDefinition {
  key: keyof RacecraftCalibration;
  unit: 'm/s' | 'm' | 's' | 'm/s^2' | 'fraction' | '1/s';
  minimum: number;
  maximum: number;
  rationale: string;
  owner: string;
}

/** Numerical resolution only; changing this must not author behavior. */
export interface RacecraftResolution {
  stationSamples: number;
}


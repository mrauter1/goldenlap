let active: Readonly<RacecraftCalibration> = RACECRAFT_CALIBRATION_DEFAULTS;
let activeResolution: Readonly<RacecraftResolution> =
  RACECRAFT_RESOLUTION_DEFAULTS;

export function racecraftCalibration(): Readonly<RacecraftCalibration> {
  return active;
}

export function racecraftResolution(): Readonly<RacecraftResolution> {
  return activeResolution;
}

function validateCalibration(calibration: Readonly<RacecraftCalibration>): void {
  for (const definition of RACECRAFT_CALIBRATION_DEFINITIONS) {
    const value = calibration[definition.key];
    if (!Number.isFinite(value) || value < definition.minimum || value > definition.maximum)
      throw new Error(
        `${definition.key} must be finite and within ` +
        `[${definition.minimum}, ${definition.maximum}] ${definition.unit}`
      );
  }
}

function validateResolution(resolution: Readonly<RacecraftResolution>): void {
  if (!Number.isInteger(resolution.stationSamples) ||
      resolution.stationSamples <= 0)
    throw new Error('stationSamples must be a positive integer');
}

/** Run one synchronous headless experiment with a bounded global override. */
export function withRacecraftCalibration<T>(
  overrides: Partial<RacecraftCalibration>,
  run: () => T
): T {
  const previous = active;
  const candidate = Object.freeze({ ...previous, ...overrides });
  validateCalibration(candidate);
  active = candidate;
  try {
    return run();
  } finally {
    active = previous;
  }
}

/** Run one synchronous convergence experiment at an alternate resolution. */
export function withRacecraftResolution<T>(
  overrides: Partial<RacecraftResolution>,
  run: () => T
): T {
  const previous = activeResolution;
  const candidate = Object.freeze({ ...previous, ...overrides });
  validateResolution(candidate);
  activeResolution = candidate;
  try {
    return run();
  } finally {
    activeResolution = previous;
  }
}
